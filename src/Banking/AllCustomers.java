    private void fillData() 
    {
       String sele="select AccountNumber,Name,PhoneNumber,Address,Balance from customerinfo";
       rs=conMe.getConnection(sele);
        try {
            if (rs.next()) 
            {
              rs.previous();
              Tmodel=m.GetModel(rs);
            }
            else
            {    
            JOptionPane.showMessageDialog(null, "There are no Customers", "Error", JOptionPane.ERROR_MESSAGE);    
            }
        } catch (SQLException ex) {
            Logger.getLogger(Deposit.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (Tmodel!=null) 
        {
            jtbl_Show.setModel(Tmodel);  
        }
    }
